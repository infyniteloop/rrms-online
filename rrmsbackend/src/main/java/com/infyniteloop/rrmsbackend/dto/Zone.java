package com.infyniteloop.rrmsbackend.dto;

public class Zone {
	
	private int id;
	private String zoneName;
	private String zonceCode;
	
	
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getZoneName() {
		return zoneName;
	}
	public void setZoneName(String zoneName) {
		this.zoneName = zoneName;
	}
	public String getZonceCode() {
		return zonceCode;
	}
	public void setZonceCode(String zonceCode) {
		this.zonceCode = zonceCode;
	}
	
	
	
	
	

}
